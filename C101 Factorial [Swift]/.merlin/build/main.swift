func countDown(n: Int) -> Int{
    print("Calculating factorial(\(n))...")
    if n <= 1 {
        print("Terminal case, returning 1.")
        return 1
    }
    else {
        let m = n * countDown(n: n-1)
        print("Non-terminal case, returning \(m).")
        return m
    }
}
print("The final result is: \(countDown(n: n)).")
